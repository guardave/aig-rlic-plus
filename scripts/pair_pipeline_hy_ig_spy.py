def write_schema(df: pd.DataFrame, path: str) -> None:
    """Write DATA-D5 schema sidecar. Only includes columns present in df."""
    present = {c: SCHEMA_COLUMNS[c] for c in df.columns if c in SCHEMA_COLUMNS}
    sidecar = {
        "pair_id": PAIR_ID,
        "parquet_path": PARQUET_PATH,
        "schema_version": "1.0.0",
        "generated_at": datetime.datetime.utcnow().strftime("%Y-%m-%dT%H:%M:%SZ"),
        "columns": present,
    }
    with open(path, "w") as fh:
        json.dump(sidecar, fh, indent=2)
    print(f"  Schema sidecar written: {path}  ({len(present)} columns)")


