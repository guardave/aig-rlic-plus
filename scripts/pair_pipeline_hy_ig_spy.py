def stage_data() -> dict:
    """
    Fetch raw series from FRED (13 series) and Yahoo Finance (10 tickers).

    Mirrors the series roster from hy_ig_v2_spy for continuity, using the
    canonical column names established in that pair's DATA-D5 sidecar.

    STOP-ON-FAILURE: if FRED or Yahoo returns no data for the core indicator
    or target, this function raises RuntimeError and the pipeline halts.
    """
    import yfinance as yf
    from fredapi import Fred

    print(f"\n{'='*60}")
    print(f"  STAGE 1 — DATA SOURCING")
    print(f"  Pair: {PAIR_ID}  |  {START_DATE} → {END_DATE}")
    print(f"{'='*60}")

    api_key = os.environ.get("FRED_API_KEY") or "952aa4d0c4b2057609fbf3ecc6954e58"
    fred = Fred(api_key=api_key)

    series: dict = {}

    # ── FRED series (13) ──────────────────────────────────────
    fred_map = [
        ("BAMLH0A0HYM2",  "hy_oas"),          # ICE BofA US HY OAS (%)
        ("BAMLC0A0CM",    "ig_oas"),           # ICE BofA US Corporate OAS (%)
        ("BAMLH0A1HYBB",  "bb_hy_oas"),        # ICE BofA BB HY OAS (%)
        ("BAMLH0A3HYC",   "ccc_hy_oas"),       # ICE BofA CCC & Lower OAS (%)
        ("DGS10",         "dgs10"),            # US 10Y Treasury CMR (%)
        ("DTB3",          "dtb3"),             # US 3M T-Bill Secondary Market (%)
        ("DGS2",          "dgs2"),             # US 2Y Treasury CMR (%)
        ("NFCI",          "nfci"),             # Chicago Fed NFCI (index, weekly)
        ("DFF",           "fed_funds_rate"),   # Effective Fed Funds Rate (%)
        ("BAMLC0A4CBBB",  "bbb_oas"),          # ICE BofA BBB Corporate OAS (%)
        ("STLFSI4",       "fsi"),              # St. Louis Fed FSI (index)
        ("ICSA",          "initial_claims"),   # Initial Jobless Claims (count, weekly)
        ("SOFR",          "sofr"),             # SOFR (%)
    ]

    for sid, name in fred_map:
        for attempt in range(3):
            try:
                s = fred.get_series(sid, observation_start=START_DATE, observation_end=END_DATE)
                series[name] = s.astype(float)
                v = s.dropna()
                print(f"  [FRED] {sid:20s} -> {name:20s}: {len(v):5d} obs  "
                      f"({v.index.min().date()} to {v.index.max().date()})")
                break
            except Exception as e:
                if attempt < 2:
                    time.sleep(1)
                else:
                    print(f"  [FRED] {sid} FAILED after 3 attempts: {e}")

    # ── OAS Series Splice (FRED truncation workaround) ────────
    # As of 2026-04-22, FRED BAMLH0A0HYM2 / BAMLC0A0CM / BAMLH0A1HYBB /
    # BAMLH0A3HYC / BAMLC0A4CBBB only return observations from 2023-04-24
    # onward (FRED series observation_start = 2023-04-24; series was truncated
    # at source). The v1 parquet (hy_ig_spy_v1_daily_20000101_20251231.parquet)
    # carries these OAS series from 2000-01-03 through 2025-12-31.
    # Strategy: back-fill pre-2023 from v1 parquet, then overlay the live
    # FRED values for the intersection and extension beyond 2025-12-31.
    # This is the same splice pattern used for SOFR in the v2 pipeline.
    oas_cols = {
        "hy_oas": "hy_oas",
        "ig_oas": "ig_oas",
        "bb_hy_oas": "bb_hy_oas",
        "ccc_hy_oas": "ccc_hy_oas",
        "bbb_oas": "bbb_oas",
    }
    v1_parquet_path = os.path.join(DATA_DIR, "hy_ig_spy_v1_daily_20000101_20251231.parquet")
    if os.path.exists(v1_parquet_path):
        v1 = pd.read_parquet(v1_parquet_path)
        for col, tgt in oas_cols.items():
            if col in v1.columns and tgt in series:
                # Combine: v1 history, then overlay with fresh FRED where available
                hist = v1[col].dropna()
                live = series[tgt].dropna()
                combined = hist.combine_first(live)   # live overrides hist on overlap
                series[tgt] = combined.astype(float)
                v = series[tgt].dropna()
                print(f"  [SPLICE] {col:20s}: v1+FRED spliced → "
                      f"{len(v):5d} obs ({v.index.min().date()} to {v.index.max().date()})")
            elif col in v1.columns and tgt not in series:
                # FRED fetch failed entirely — fall back to v1 only
                series[tgt] = v1[col].astype(float)
                v = series[tgt].dropna()
                print(f"  [SPLICE] {col:20s}: v1 only (FRED failed) → "
                      f"{len(v):5d} obs ({v.index.min().date()} to {v.index.max().date()})")
    else:
        print(f"  [WARN] v1 parquet not found at {v1_parquet_path}; OAS history limited to FRED coverage.")

    # Halt on missing core indicator
    if "hy_oas" not in series or "ig_oas" not in series:
        raise RuntimeError(
            "STOP: Could not source BAMLH0A0HYM2 or BAMLC0A0CM "
            "(core HY-IG OAS series) from FRED or v1 splice. "
            "Cannot build hy_ig_spread_pct. Check network / API key and rerun."
        )

    # ── Yahoo Finance (10 tickers) ────────────────────────────
    yf_map = [
        ("SPY",       "spy"),
        ("^VIX",      "vix"),
        ("^VIX3M",    "vix3m"),
        ("KBE",       "kbe"),
        ("IWM",       "iwm"),
        ("^MOVE",     "move_index"),
        ("GC=F",      "gold"),
        ("HG=F",      "copper"),
        ("DX-Y.NYB",  "dxy"),
        ("HYG",       "hyg"),
    ]

    for ticker, name in yf_map:
        try:
            dl = yf.download(ticker, start=START_DATE, end=END_DATE,
                             progress=False, auto_adjust=True)
            if isinstance(dl.columns, pd.MultiIndex):
                dl.columns = dl.columns.get_level_values(0)
            s = dl["Close"]
            s.index = s.index.tz_localize(None) if s.index.tz else s.index
            series[name] = s.astype(float)
            print(f"  [YF]   {ticker:12s} -> {name:15s}: {len(s):5d} obs  "
                  f"({s.index.min().date()} to {s.index.max().date()})")
        except Exception as e:
            print(f"  [YF]   {ticker} FAILED: {e}")

    # Halt on missing target
    if "spy" not in series:
        raise RuntimeError(
            "STOP: Yahoo Finance fetch failed for SPY (target asset). "
            "Cannot build hy_ig_spy pair. Check network and rerun."
        )

    print(f"\n  Total series sourced: {len(series)}")
    return series


