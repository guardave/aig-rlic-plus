# ─────────────────────────────────────────────────────────────
# DATA-D12: COLUMN-SUFFIX LINTER (pre-save gate)
# ─────────────────────────────────────────────────────────────

# Columns that are unit-valued and require a canonical suffix.
# Linter checks that every numeric column in the DataFrame either:
#   (a) is a date/index column, or
#   (b) carries one of the approved suffixes.
CANONICAL_SUFFIXES = (
    "_bps", "_pct", "_ratio", "_usd",
    "_pct_mom", "_pct_yoy", "_ret", "_vol_ann_pct", "_idx",
    "_oas",    # spread levels quoted in percent (OAS: _oas suffix permitted per catalog)
    "_zscore_252d", "_zscore_504d",   # z-score composites (carry _ratio semantics)
    "_pctrank_504d", "_pctrank_1260d",
    "_roc_21d", "_roc_63d", "_roc_126d",    # rate-of-change in percent
    "_mom_21d", "_mom_63d", "_mom_252d",    # momentum in pp
    "_acceleration",                         # derived spread acceleration
    "_realized_vol_21d",                     # realized vol (decimal ratio)
    "_term_structure",                       # derived index difference
    "_momentum_13w",                         # NFCI index momentum
    "_fwd_1d", "_fwd_5d", "_fwd_21d",        # forward returns (decimal)
    "_fwd_63d", "_fwd_126d", "_fwd_252d",
)

# Non-signal numeric columns exempt from suffix rule (prices, raw index levels,
# raw FRED series that are consumed exclusively through derived columns).
# Raw FRED rates/indices are kept under their canonical short names (dgs10, dtb3, etc.)
# because renaming them would break downstream Evan stage expectations. Their units
# are declared in the DATA-D5 sidecar instead.
SUFFIX_EXEMPT = {
    "spy", "vix", "vix3m", "kbe", "iwm", "gold", "copper", "dxy", "hyg",
    "move_index",
    # Raw FRED rates (percent) — exempt; units declared in schema sidecar
    "dgs10", "dtb3", "dgs2", "nfci", "fed_funds_rate", "fsi", "initial_claims", "sofr",
    # Raw OAS levels (percent) — exempt; units declared in schema sidecar
    "hy_oas", "ig_oas", "bb_hy_oas", "ccc_hy_oas", "bbb_oas",
    # Derived index-unit columns — exempt (no unit-suffix convention for index levels)
    "vix_term_structure", "nfci_momentum_13w",
}

def lint_column_suffixes(df: pd.DataFrame) -> list:
    """
    DATA-D12 pre-save linter.
    Returns a list of violation strings. Empty list = PASS.
    """
    violations = []
    for col in df.columns:
        if col == "date":
            continue
        if col in SUFFIX_EXEMPT:
            continue
        if not any(col.endswith(s) for s in CANONICAL_SUFFIXES):
            violations.append(
                f"Column '{col}' stores unit-valued data but has no canonical unit "
                f"suffix (_bps/_pct/_ratio/etc.). DATA-D12 violation."
            )
    return violations


