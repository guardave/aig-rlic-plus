def main():
    t0 = time.time()
    print(f"\n{'='*60}")
    print(f"  {INDICATOR} → {TARGET}")
    print(f"  Pair ID: {PAIR_ID}")
    print(f"  Wave 10G.4A — Data Stage  |  {END_DATE}")
    print(f"{'='*60}")

    # Stage 1: fetch
    series = stage_data()

    # Stage 2: derive
    df = stage_derived(series)

    # DATA-D12: pre-save column-suffix linter
    print(f"\n  [DATA-D12] Running column-suffix linter …")
    violations = lint_column_suffixes(df)
    if violations:
        for v in violations:
            print(f"  [DATA-D12 VIOLATION] {v}")
        raise RuntimeError(
            f"DATA-D12 pre-save linter found {len(violations)} violation(s). "
            "Rename columns before saving. Pipeline halted."
        )
    else:
        print(f"  [DATA-D12] PASS — all {len(df.columns)} columns conform.")

    # Save parquet
    df.to_parquet(PARQUET_PATH, engine="pyarrow")
    size_kb = os.path.getsize(PARQUET_PATH) / 1024
    print(f"\n  Saved master parquet : {PARQUET_PATH}")
    print(f"  Rows: {len(df)}  Cols: {df.shape[1]}  Size: {size_kb:.1f} KB")
    print(f"  Date range: {df.index.min().date()} to {df.index.max().date()}")

    # DATA-D5: schema sidecar
    write_schema(df, SCHEMA_PATH)

    elapsed = time.time() - t0
    print(f"\n  Data stage complete in {elapsed:.1f}s")
    print(f"  Parquet : {PARQUET_PATH}")
    print(f"  Schema  : {SCHEMA_PATH}")
    return df


if __name__ == "__main__":
    main()
