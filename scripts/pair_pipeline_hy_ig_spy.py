def stage_derived(series: dict) -> pd.DataFrame:
    """
    Align raw series to business-day calendar and build all derived
    signals. Column naming follows DATA-D12 suffix conventions.

    Derived columns produced:
    - hy_ig_spread_pct        : HY OAS − IG OAS (pct)
    - hy_ig_zscore_252d/504d  : rolling z-scores (ratio)
    - hy_ig_pctrank_504d/1260d: rolling percentile ranks (ratio)
    - hy_ig_roc_21/63/126d    : rate-of-change (pct)
    - hy_ig_mom_21/63/252d    : level momentum (pct units)
    - hy_ig_acceleration      : 2nd-diff proxy (pct units)
    - ccc_bb_spread           : within-HY quality spread (pct)
    - hy_ig_realized_vol_21d  : annualized vol of spread changes (ratio)
    - vix_term_structure      : VIX3M − VIX (index units)
    - yield_spread_10y3m      : 10Y − 3M (pct)
    - yield_spread_10y2y      : 10Y − 2Y (pct)
    - bank_smallcap_ratio     : KBE / IWM (ratio)
    - nfci_momentum_13w       : 13-week NFCI change (index)
    - bbb_ig_spread           : BBB − IG OAS (pct)
    - spy_ret                 : SPY daily return (decimal)
    - spy_fwd_{1/5/21/63/126/252}d : forward returns (decimal)
    """
    print(f"\n{'='*60}")
    print(f"  STAGE 2 — DERIVED SIGNALS")
    print(f"{'='*60}")

    bdays = pd.bdate_range(START_DATE, END_DATE)
    df = pd.DataFrame(index=bdays)
    df.index.name = "date"

    weekly_cols = {"nfci", "fsi", "initial_claims"}

    all_raw_cols = [
        "hy_oas", "ig_oas", "bb_hy_oas", "ccc_hy_oas",
        "dgs10", "dtb3", "dgs2", "nfci", "fed_funds_rate",
        "bbb_oas", "fsi", "initial_claims", "sofr",
        "spy", "vix", "vix3m", "kbe", "iwm",
        "move_index", "gold", "copper", "dxy", "hyg",
    ]

    for col in all_raw_cols:
        if col not in series:
            print(f"  [WARN] raw column '{col}' not in fetched series — skipped.")
            continue
        s = series[col].copy()
        limit = 10 if col in weekly_cols else 5
        if col in weekly_cols:
            # Shift weekend release dates to preceding Friday for bday alignment
            def _to_bday(d):
                if d.weekday() < 5:
                    return d
                return d - pd.Timedelta(days=d.weekday() - 4)
            s.index = s.index.map(_to_bday)
            s = s[~s.index.duplicated(keep="last")]
        df[col] = s.reindex(bdays).ffill(limit=limit)

    # ── Core indicator: HY-IG spread in percent ───────────────
    df["hy_ig_spread_pct"] = df["hy_oas"] - df["ig_oas"]
    spread = df["hy_ig_spread_pct"]

    # ── Z-scores ──────────────────────────────────────────────
    df["hy_ig_zscore_252d"] = (
        (spread - spread.rolling(252, min_periods=200).mean())
        / spread.rolling(252, min_periods=200).std()
    )
    df["hy_ig_zscore_504d"] = (
        (spread - spread.rolling(504, min_periods=400).mean())
        / spread.rolling(504, min_periods=400).std()
    )

    # ── Percentile ranks ──────────────────────────────────────
    df["hy_ig_pctrank_504d"] = spread.rolling(504, min_periods=400).apply(
        lambda x: stats.rankdata(x)[-1] / len(x), raw=True
    )
    df["hy_ig_pctrank_1260d"] = spread.rolling(1260, min_periods=1000).apply(
        lambda x: stats.rankdata(x)[-1] / len(x), raw=True
    )

    # ── Rates-of-change (%) ───────────────────────────────────
    df["hy_ig_roc_21d"]  = (spread / spread.shift(21)  - 1) * 100
    df["hy_ig_roc_63d"]  = (spread / spread.shift(63)  - 1) * 100
    df["hy_ig_roc_126d"] = (spread / spread.shift(126) - 1) * 100

    # ── Momentum (absolute pp change) ────────────────────────
    df["hy_ig_mom_21d"]  = spread - spread.shift(21)
    df["hy_ig_mom_63d"]  = spread - spread.shift(63)
    df["hy_ig_mom_252d"] = spread - spread.shift(252)

    # ── Acceleration (2nd-diff proxy) ────────────────────────
    df["hy_ig_acceleration"] = df["hy_ig_roc_21d"] - df["hy_ig_roc_21d"].shift(21)

    # ── Quality spread: CCC − BB ──────────────────────────────
    # DATA-D12: column suffixed _pct (spread in percentage points)
    if "ccc_hy_oas" in df.columns and "bb_hy_oas" in df.columns:
        df["ccc_bb_spread_pct"] = df["ccc_hy_oas"] - df["bb_hy_oas"]
    else:
        print("  SKIP ccc_bb_spread_pct: missing ccc_hy_oas or bb_hy_oas")

    # ── Realized volatility of spread changes ─────────────────
    df["hy_ig_realized_vol_21d"] = spread.diff().rolling(21, min_periods=15).std()

    # ── Cross-market derived signals ──────────────────────────
    def _safe_sub(a, b, name):
        if a in df.columns and b in df.columns:
            df[name] = df[a] - df[b]
        else:
            print(f"  SKIP {name}: missing {a if a not in df.columns else b}")

    def _safe_div(a, b, name):
        if a in df.columns and b in df.columns:
            df[name] = df[a] / df[b]
        else:
            print(f"  SKIP {name}: missing {a if a not in df.columns else b}")

    # DATA-D12: derived spread/ratio columns carry _pct or _ratio suffixes
    _safe_sub("vix3m",  "vix",    "vix_term_structure")     # index units, exempt
    _safe_sub("dgs10",  "dtb3",   "yield_spread_10y3m_pct")
    _safe_sub("dgs10",  "dgs2",   "yield_spread_10y2y_pct")
    _safe_div("kbe",    "iwm",    "bank_smallcap_ratio")
    if "nfci" in df.columns:
        df["nfci_momentum_13w"] = df["nfci"] - df["nfci"].shift(65)
    _safe_sub("bbb_oas", "ig_oas", "bbb_ig_spread_pct")

    # ── Forward SPY returns (6 horizons) ─────────────────────
    spy = df["spy"]
    df["spy_ret"]     = spy.pct_change()
    df["spy_fwd_1d"]  = spy.pct_change(1).shift(-1)
    df["spy_fwd_5d"]  = spy.shift(-5)  / spy - 1
    df["spy_fwd_21d"] = spy.shift(-21) / spy - 1
    df["spy_fwd_63d"] = spy.shift(-63) / spy - 1
    df["spy_fwd_126d"]= spy.shift(-126)/ spy - 1
    df["spy_fwd_252d"]= spy.shift(-252)/ spy - 1

    # ── Drop rows missing core variables ─────────────────────
    df = df.dropna(subset=["hy_ig_spread_pct", "spy"])

    print(f"  Master DataFrame: {df.shape[0]} rows × {df.shape[1]} cols")
    print(f"  Date range: {df.index.min().date()} to {df.index.max().date()}")

    key_cols = ["hy_ig_spread_pct", "hy_ig_zscore_252d", "hy_ig_roc_21d", "spy", "vix"]
    for c in key_cols:
        if c in df.columns:
            pct_missing = df[c].isna().mean() * 100
            print(f"  Missing: {c:32s} {pct_missing:.1f}%")

    return df


